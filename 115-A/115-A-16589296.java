import java.util.*;
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////SOLUTION ///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////




public  class Solution

{    
   //main
	public static void main(String[] args){
	Scanner in=new Scanner (System.in) ;
	int n=in.nextInt();
	int s=0,max=0;
	
	int[] rel=new int[n+1];
	for( int i=1;  i<=n;  i++)
		rel[i] = in.nextInt();
	
	for(int i=1; i<=n; i++){
		s=1;
		int hash = rel[i];
		while(hash != -1)
		{
			s++;
			hash = rel[hash];
		}
		max=Math.max(max,s);
	}
   
	System.out.println(max);
}//void main

}//class main